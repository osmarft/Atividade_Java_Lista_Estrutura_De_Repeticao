
package Lista_2_Estrutura_De_Decisao;

public class Exercicio16 {
	private int codigoPagamento;
	private Double precoEtiqueta;
	
	public Exercicio16(int codigoPagamento, Double recoEtiqueta) {
		
		setCodigoPagamento(codigoPagamento);
		setPrecoEtiqueta(recoEtiqueta);
	}
	public Exercicio16() {
		super();
	}
	public int getCodigoPagamento() {
		return codigoPagamento;
	}
	public void setCodigoPagamento(int codigoPagamento) {
		this.codigoPagamento = codigoPagamento;
	}
	public Double getPrecoEtiqueta() {
		return precoEtiqueta;
	}
	public void setPrecoEtiqueta(Double precoEtiqueta) {
		this.precoEtiqueta = precoEtiqueta;
	}
	
		
 
}
